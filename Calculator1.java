public class Calculator1{
    public static void main(String[] args) {
        double operand1 = 10; // Example operands
        double operand2 = 5;
        
        double additionResult = operand1 + operand2;
        double subtractionResult = operand1 - operand2;
        double multiplicationResult = operand1 * operand2;
        double divisionResult = operand1 / operand2;
        
        System.out.println("Addition result: " + additionResult);
        System.out.println("Subtraction result: " + subtractionResult);
        System.out.println("Multiplication result: " + multiplicationResult);
        System.out.println("Division result: " + divisionResult);
    }
}